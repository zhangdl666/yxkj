namespace Pc\Controller;
/**
 * 维修类型
 * Class RepairTypeController
 * @package Pc\Controller
 */
class RepairTypeController extends BaseController
{
    public $model = 'equipment_oper';


    protected function _set_wheres(&$wheres)
    {
        $wheres = array('type' => array('eq', 2));
    }

    public function _initialize()
    {
        parent::_initialize(); // TODO: Change the autogenerated stub
    }

    public function index()
    {
        $this->assign('name','维修');
        $this->assign('equipment', '维修');
        parent::index();
    }

    public function add()
    {
        $this->assign('type', 2);
        $this->assign('equipment', '维修');
        $this->display('edit');
    }
}